package br.ufpe.eonsimulator.modulation;

public class ModulationFormatBitRateWrapper {

	private IsModulationFormat modulationFormat;
	private double bitRate;
	private double slotWidth;

	public ModulationFormatBitRateWrapper(IsModulationFormat modulationFormat,
			double bitRate, double slotWidth) {
		super();
		this.modulationFormat = modulationFormat;
		this.bitRate = bitRate;
		this.slotWidth = slotWidth;
	}

	public IsModulationFormat getModulationFormat() {
		return modulationFormat;
	}

	public void setModulationFormat(IsModulationFormat modulationFormat) {
		this.modulationFormat = modulationFormat;
	}

	public double getBitRate() {
		return bitRate;
	}

	public void setBitRate(double bitRate) {
		this.bitRate = bitRate;
	}

	public double getSlotWidth() {
		return slotWidth;
	}

	public void setSlotWidth(double slotWidth) {
		this.slotWidth = slotWidth;
	}

}
