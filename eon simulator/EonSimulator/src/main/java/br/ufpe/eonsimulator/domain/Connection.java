package br.ufpe.eonsimulator.domain;

import br.ufpe.simulatorkernel.domain.PhysicalElementPair;

public class Connection {
	private PhysicalElementPair physicalElementPair;
	private int numberSlotRequired;
	private double requestedBitRate;
	private double disconnectionTime;
	private int maxSlotsExpandUnits;
	private double requiredOSNR;
	private Route route;

	public int getNumberSlotRequired() {
		return numberSlotRequired;
	}

	public void setNumberSlotRequired(int numberSlotRequired) {
		this.numberSlotRequired = numberSlotRequired;
	}

	public double getDisconnectionTime() {
		return disconnectionTime;
	}

	public void setDisconnectionTime(double d) {
		this.disconnectionTime = d;
	}

	public double getRequestedBitRate() {
		return requestedBitRate;
	}

	public void setRequestedBitRate(double requestedBitRate) {
		this.requestedBitRate = requestedBitRate;
	}

	public int getMaxSlotsExpandUnits() {
		return maxSlotsExpandUnits;
	}

	public void setMaxSlotsExpandUnits(int maxSlotsExpandUnits) {
		this.maxSlotsExpandUnits = maxSlotsExpandUnits;
	}

	public PhysicalElementPair getPhysicalElementPair() {
		return physicalElementPair;
	}

	public void setPhysicalElementPair(PhysicalElementPair physicalElementPair) {
		this.physicalElementPair = physicalElementPair;
	}

	public void setRequiredOSNR(double requiredOSNR) {
		this.requiredOSNR = requiredOSNR;
	}

	public double getRequiredOSNR() {
		return requiredOSNR;
	}

	public void setRoute(Route route) {
		this.route = route;
		
	}

	public Route getRoute() {
		return route;
	}
}
